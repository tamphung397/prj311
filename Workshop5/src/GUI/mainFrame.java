    void loadData() {
        h = DAO.FileDAO.loadSmartFromFile(filename);
        root = new DefaultMutableTreeNode("Brand");
        DefaultTreeModel model = (DefaultTreeModel) jTree1.getModel();
        model.setRoot(root);
        Set<Brand> keys = h.keySet();
        for (Brand key : keys) {
            DefaultMutableTreeNode node = new DefaultMutableTreeNode(key);
            root.add(node);
            ArrayList<SmartPhone> list = h.get(key);
            for (SmartPhone smartPhone : list) {
                DefaultMutableTreeNode child = new DefaultMutableTreeNode(smartPhone);
                node.add(child);
            }
        }

    }

