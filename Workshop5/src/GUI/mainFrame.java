    private void jTree1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTree1MouseClicked
        

        TreePath path = jTree1.getSelectionPath();
        if (path == null) {
            return;
        }
        DefaultMutableTreeNode selectedNode = null;
        selectedNode = (DefaultMutableTreeNode) path.getLastPathComponent();
        Object selectedObj = selectedNode.getUserObject();

        if (selectedNode == root) {
            curBrand = curSmart = null;
        } else {
            if (selectedObj instanceof Brand) {
                curBrand = selectedNode;
                curSmart = null;
            } else if (selectedObj instanceof SmartPhone) {
                curSmart = selectedNode;
                curBrand = (DefaultMutableTreeNode) selectedNode.getParent();
            }
        }
        viewBeandAndSmart();

    }//GEN-LAST:event_jTree1MouseClicked


    boolean validation() {
        String code = txtCode.getText().toUpperCase();
        String name = txtName.getText().trim();
        String made = txtPlace.getText().trim();

//        Integer salary = Integer.parseInt(txtSalary.getText().toString());
        if (code.isEmpty() || name.isEmpty() || made.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please  fill infpormation", null, JOptionPane.WARNING_MESSAGE);
            return false;

        } else if (!txtCode.getText().toUpperCase().matches("S[0-9]{4}")) {
            JOptionPane.showMessageDialog(null, "Please input format for ID is Sxxxx(x be long to 0-9)", null, JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;

    }
