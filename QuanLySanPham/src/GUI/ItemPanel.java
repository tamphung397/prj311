    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(txtMaMH.getText().trim());
            String name = txtTenMH.getText();
            int price = (int) spinnerGia.getValue();
            String supplier = (String) cbxSuplier.getSelectedItem();
            int idSupplier = 0;
            if (supplier.equalsIgnoreCase("coca")) {
                idSupplier = 0;
            } else if (supplier.equalsIgnoreCase("larue")) {
                idSupplier = 1;
            } else if (supplier.equalsIgnoreCase("7up")) {
                idSupplier = 2;
            }
            if (themmoi) {
                if (DAO.SupplierDAO.insertProduct(id, name, price, idSupplier) != 0) {
                    JOptionPane.showMessageDialog(null, "Them thanh cong");
                    loadItem();
                    DefaultComboBoxModel model = (DefaultComboBoxModel) tableSanPham.getModel();
                    
                } else {
                    JOptionPane.showMessageDialog(null, "Them that bai");
                }
                txtMaMH.setEnabled(false);
                txtTenMH.setEnabled(false);
                spinnerGia.setEnabled(false);
                cbxSuplier.setEnabled(false);
                txtMaMH.setText("");
                txtTenMH.setText("");
            }else{
                Vector<Product> list = DAO.SupplierDAO.getProduct();
                int pos = tableSanPham.getSelectedRow();
                Product p = list.get(pos);
                int oldId = p.getId();
                if(DAO.SupplierDAO.updateProduct(id, name, price, idSupplier,oldId) != 0){
                     JOptionPane.showMessageDialog(null, "update thanh cong");
                    loadItem();
                }else{
                    JOptionPane.showMessageDialog(null, "update that bai");
                }
            }
        } catch (Exception e) {
               if (e.getMessage().contains("Cannot insert duplicate key")) {
                JOptionPane.showMessageDialog(null, "ID trung");
            } else {
                JOptionPane.showMessageDialog(null, "Ma, ten nha cung cap bi sai");
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed

