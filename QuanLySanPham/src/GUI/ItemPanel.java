    private void tableSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSanPhamMouseClicked
        // TODO add your handling code here:
        spinnerGia.setEnabled(false);
        txtMaMH.setEnabled(false);
        txtTenMH.setEnabled(false);
        cbxSuplier.setEnabled(false);
        int pos = tableSanPham.getSelectedRow();
        int code = (int) tableSanPham.getValueAt(pos, 0);
        Vector<Product> list = DAO.SupplierDAO.getProduct();
        Product p = null;
        for (Product product : list) {
            if (product.getId() == code) {
                p = product;
                break;
            }
        }
        if (pos >= 0) {
            txtMaMH.setText(((Integer) tableSanPham.getValueAt(pos, 0)).toString());
            txtTenMH.setText((String) tableSanPham.getValueAt(pos, 1));
            spinnerGia.setValue(tableSanPham.getValueAt(pos, 2));
            
            DefaultComboBoxModel dd = (DefaultComboBoxModel) cbxSuplier.getModel();
            
            dd.setSelectedItem(p.getSupplier());
            ;
        }
    }//GEN-LAST:event_tableSanPhamMouseClicked

