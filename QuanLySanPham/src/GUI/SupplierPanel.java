    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(txtID.getText().trim());

            String name = txtTenNCC.getText();

            if (themmoi) {
                if (DAO.SupplierDAO.insertSupplier(id, name) != 0) {
                    JOptionPane.showMessageDialog(null, "Them thanh cong");
                    loadSuplier();
                } else {
                    JOptionPane.showMessageDialog(null, "Them that bai");
                }
                txtID.setEnabled(false);
                txtTenNCC.setEnabled(false);

            } else {
                Vector<Supplier> list = DAO.SupplierDAO.getSupplier();
                int pos = tableSanPham.getSelectedRow();
                Supplier s = list.get(pos);

                int OldId = s.getId();
//                System.out.println(id);
//                System.out.println(name);

                if (DAO.SupplierDAO.updateSupplier(id, name, OldId) != 0) {
                    JOptionPane.showMessageDialog(null, "update thanh cong");
                    loadSuplier();
                } else {
                    JOptionPane.showMessageDialog(null, "Them that bai");
                }
                txtTenNCC.setEnabled(false);
            }

        } catch (Exception e) {
            if (e.getMessage().contains("Cannot insert duplicate key")) {
                JOptionPane.showMessageDialog(null, "ID trung");
            } else {
                JOptionPane.showMessageDialog(null, "Ma, ten nha cung cap bi sai");
            }

        }
    }//GEN-LAST:event_btnLuuActionPerformed

