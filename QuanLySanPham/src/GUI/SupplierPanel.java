    private void tableSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSanPhamMouseClicked
        // TODO add your handling code here:
        txtID.setEnabled(false);
        txtTenNCC.setEnabled(false);

        int pos = tableSanPham.getSelectedRow();
//        System.out.println(pos);
        int code = (int) tableSanPham.getValueAt(pos, 0);
//        System.out.println(code);
        Vector<Supplier> list = DAO.SupplierDAO.getSupplier();
        Supplier s = null;
        for (Supplier supplier : list) {
            if (supplier.getId() == code) {
                s = supplier;
                break;
            }
        }
        if (pos >= 0) {
            txtID.setText(((Integer) tableSanPham.getValueAt(pos, 0)).toString());
            txtTenNCC.setText((String) tableSanPham.getValueAt(pos, 1));
        }

    }//GEN-LAST:event_tableSanPhamMouseClicked

