    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            ser = new ServerSocket(9999);
            JOptionPane.showMessageDialog(null, "Server started");
            copy = ser.accept();// neu client connect dc toi 999 thi copy khac null
            // copy la socket copy cua client
            
            if(copy!=null){
                isSer = copy.getInputStream();
                osSer = copy.getOutputStream();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            if(isSer != null){
                byte[] kq = new byte[isSer.available()];// tra ve kich thuoc
                isSer.read(kq);
                String msg = new String(kq); // doi byte thanh chuoi
                if(!msg.trim().isEmpty())
                jTextArea1.setText(""+ jTextArea1.getText()+"\nClient:"+ msg);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton3ActionPerformed

