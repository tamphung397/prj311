package DTO;

/**
 *
 * @author USER
 */
public class Stock {
    private String code;
    private String name;
    private String date;
    private String address;

    public Stock(String code, String name, String date, String address) {
        this.code = code;
        this.name = name;
        this.date = date;
        this.address = address;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    @Override
    public String toString() {
        return code + ";" + name +";" + address + ";" + date;
    }
    
}
