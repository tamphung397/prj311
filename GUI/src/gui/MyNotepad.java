    private void MnuNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MnuNewActionPerformed
        // TODO add your handling code here:
        String s = jtxtContent.getText();
        if (!s.isEmpty()) {
            // luu noi dung cu lai truoc khi new moi
            JFileChooser fc = new JFileChooser();
            int result = fc.showSaveDialog(null);
            if (result == JFileChooser.APPROVE_OPTION) {
                // ghi text file
                // lay ten file user dachon trong JflieChooser
                File f = fc.getSelectedFile();
                if (f.exists()) {
                    int ans = JOptionPane.showConfirmDialog(null, "Do you want to replace all", "Hoi ngu ti", JOptionPane.YES_NO_OPTION);
                    if (ans == JOptionPane.YES_OPTION) {
                        JOptionPane.showMessageDialog(null, "ghet thang kia");
                    } else {
                        JOptionPane.showMessageDialog(null, "thuong thang kia");
                    }
                }
                PrintWriter w = null;
                try {
                    w = new PrintWriter(f);
                    w.println(s);
                    jtxtContent.setText("");
                } catch (Exception e) {
                    e.printStackTrace();
                } finally {
                    try {
                        if (w != null) {
                            w.close();
                        }
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
            } else {
                jtxtContent.setText("");
            }
        }
  
    }//GEN-LAST:event_MnuNewActionPerformed

