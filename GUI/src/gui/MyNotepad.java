    private void mnuOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuOpenActionPerformed
        // TODO add your handling code here:
        String s = jtxtContent.getText();
        if (!s.isEmpty()) {
            // neu ope n file ma noi dung cu dang co ma can luu lai noi dung nay copy code mnNew vao day

        } else {
            JFileChooser fc = new JFileChooser();
            if (fc.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
                File f = fc.getSelectedFile();
                FileReader ff = null;
                BufferedReader r = null;
                try {
                    setTitle(f.getName());
                    ff = new FileReader(f);
                    r = new BufferedReader(ff);
                    while (r.ready()) {
                        jtxtContent.setText(jtxtContent.getText() + r.readLine());
                    }

                } catch (Exception e) {
                    e.printStackTrace();
                } finally {
                    try {
                        if (ff != null) {
                            ff.close();
                        }
                        if (r != null) {
                            r.close();
                        }
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
            }
         
    
            
        }
    }//GEN-LAST:event_mnuOpenActionPerformed

