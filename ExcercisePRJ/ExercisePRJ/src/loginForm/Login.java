    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String pass = jPasswordField1.getText();
        String username = jTextField1.getText();
        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Name not Empty!");
        }
        try {
            if (!pass.matches("[a-zA-z0-9 ]{6,12}")) {
                throw new Exception("PW must a-z A-z 0-9, 6 to 12 Char");
            }
            else{
                JOptionPane.showMessageDialog(null, "Login Success!! Congratulation!!!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "PW must a-z A-z 0-9, 6 to 12 Char");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

